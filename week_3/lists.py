# A list is an ordered collection of data
# Lists can be mutable or immutable

items = [ " pen","ruler","set","laptop"]
print(items)

#append
#insert
#remove
#index
#count
#sort
#reverse
#pop -

#APPEND - adds onto the list
items.append("phone")
print("\n")
print(items)

#POP - removes appended item on list
items.pop()
print("\n")
print(items)

# COUNT
items.count(items)
print("\n")
print(items)

#REMOVE - removes the stated item form the list
items.remove("set")
print("\n")
print(items)

#REVERSE -reverses the ordeer of the items on the list
items.reverse()
print("\n")
print(items)









